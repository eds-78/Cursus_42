char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[j])
	{
		 if (s[j] == c)
		 	i = j;
		j++;
	}
	if (i == 0)
		return (0);
	else
		return ((char *)s + i);
}
